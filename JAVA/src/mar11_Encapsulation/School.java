package mar11_Encapsulation;

public class School {

	private String nameOfTheSchool;
	private String nameOfTheHM;
	private String address;
	private int phoneNo;
	private int noOfStudents;
	private String hallTicketNo;
	private int number;
	private String nameOfStu;
	private String college;

	public String getcollege() {
		return college;
	}

	public void setcollege(String college) {

		this.college = college;
	}

	public String getnameOfStu() {
		return nameOfStu;
	}

	public void setnameOfStu(String nameOfStu) {
		this.nameOfStu = nameOfStu;
	}

	public String getNameOfTheSchool() {
		return nameOfTheSchool;
	}

	public void setNameOfTheSchool(String nameOfTheSchool) {
		this.nameOfTheSchool = nameOfTheSchool;
	}

	public String getNameOfTheHM() {
		return nameOfTheHM;
	}

	public void setNameOfTheHM(String nameOfTheHM) {
		this.nameOfTheHM = nameOfTheHM;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public int getPhoneNo() {
		return phoneNo;
	}

	public void setPhoneNo(int phoneNo) {
		this.phoneNo = phoneNo;
	}

	public int getNoOfStudents() {
		return noOfStudents;
	}

	public void setNoOfStudents(int noOfStudents) {
		this.noOfStudents = noOfStudents;
	}

	public String getHallTicketNo() {
		return hallTicketNo;
	}

	public void setHallTicketNo(String hallTicketNo) {
		this.hallTicketNo = hallTicketNo;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

}
